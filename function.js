
function week( deen){

    if( deen == 0){
        return`Sunday`
    }else if( deen == 1){
        return`Monday`
    }else if( deen == 2){
        return`Tuesday`
    }else if( deen == 3){
        return`Wednesday`
    }else if( deen == 4){
        return`Thursday`
    }else if( deen == 5){
        return`Friday`
    }else if( deen == 6){
        return`Saturday`
    }
}